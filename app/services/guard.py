"""Guard от плохих финальных реплик слабой модели.

Два независимых сбоя, оба до отправки:

1. Офтоп (is_on_topic): модель игнорирует последнее сообщение и выдаёт длинную
   реплику из другого контекста (реальный случай: на вопрос про еду — простыня
   про день рождения и рабочие шлюги). Внешне — «увёл чужую переписку», хотя
   изоляция цела; это конфабуляция. Спрашиваем у модели, по существу ли ответ,
   и при явном промахе даём один шанс переписать строго по теме.

2. Отложенная работа (is_deferral): бот синхронный — «в фоне» он ничего не
   делает. Если модель на просьбу что-то найти/сделать пишет «сейчас поищу…,
   дай мне время» и НЕ вызывает ни одного инструмента — это пустое обещание,
   к задаче никто не вернётся (реальный случай: «заресерчи доставку в
   Подгорице» → план на будущее без единого web_search). Детект дешёвый и
   детерминированный (регэксп), без LLM-вызова; вызывающий даёт модели ещё
   один tool-раунд с жёстким наджем «сделай сейчас через инструменты».

Fail-open: любая ошибка/неоднозначность проверки → считаем ответ валидным, чтобы
guard никогда не глотал нормальные реплики.
"""
import logging
import re

from app.llm import client

logger = logging.getLogger("gennady.guard")

# Короткие реплики («ок», «ха, точно», эмодзи) проверять и бессмысленно, и
# ненадёжно — а сбойный случай всегда длинный. Так отсекаем большинство ходов.
MIN_LEN_TO_CHECK = 200

_CHECK_PROMPT = (
    "Ты — контролёр реплик чат-бота в живой переписке. Пользователь написал "
    "последнее сообщение, бот сгенерировал ответ. Отвечает ли ответ ПО СУЩЕСТВУ "
    "именно на это сообщение?\n"
    "Считай нормой: короткие реплики, шутки, уточняющие вопросы, продолжение "
    "недавней темы разговора, отказ помочь.\n"
    "Провал (OFFTOPIC) — только явный: ответ про совершенно другую тему, будто "
    "из чужого разговора (например, поздравляет с днём рождения или обсуждает "
    "работу в ответ на вопрос про еду).\n\n"
    "Последнее сообщение пользователя:\n«{user}»\n\n"
    "Ответ бота:\n«{reply}»\n\n"
    "Ответь РОВНО одним словом: OK или OFFTOPIC."
)

REWRITE_NUDGE = (
    "[Системное: твой предыдущий ответ был не по теме — он не отвечал на последнее "
    "сообщение пользователя, а ушёл в постороннюю тему. Ответь заново, строго по "
    "последнему сообщению пользователя. Не придумывай событий, поздравлений и тем, "
    "которых в этом сообщении не было.]"
)


async def is_on_topic(
    user_text: str, reply_text: str, model: str
) -> tuple[bool, client.LlmResult | None]:
    """(по теме?, usage проверки). Пустой ввод / короткий ответ / сбой → (True, None)."""
    if not user_text.strip() or len(reply_text.strip()) < MIN_LEN_TO_CHECK:
        return True, None
    prompt = _CHECK_PROMPT.format(user=user_text[:1500], reply=reply_text[:1500])
    try:
        result = await client.chat([{"role": "user", "content": prompt}], model)
    except Exception as exc:  # noqa: BLE001 — сбой проверки не должен ломать ответ
        logger.warning("Guard-проверка не удалась: %s", exc)
        return True, None
    verdict = (result.content or "").strip().upper()
    on_topic = "OFFTOPIC" not in verdict
    if not on_topic:
        logger.info("Guard: ответ признан не в тему, перегенерирую")
    return on_topic, result


# Фразы-обещания будущей работы. Ловим именно намерение «сделаю позже / сейчас
# начну искать», а не «нашёл»: слабая модель проговаривает план вместо вызова
# инструмента. Проверяется ТОЛЬКО когда за ход не было ни одного инструмента —
# иначе это нормальные слова по ходу реальной работы.
# Глаголы будущего действия, которыми модель откладывает работу («сейчас
# составлю», «пришлю список», «подготовлю» и т.п.). Список широкий намеренно:
# регэксп-подход обречён отставать от новых формулировок, поэтому лучше ловить
# с запасом — риск ложняка гасит условие «инструментов за ход не было».
_DEFER_VERB = (
    r"(?:со)?ставлю|пришл[юё]|вышлю|скину|отправлю|подготовлю|сформирую|соберу"
    r"|найду|поищу|посмотрю|погуглю|погляжу|гляну|проверю|уточню|сделаю|покажу"
    r"|займ[уё]сь|начну|поработаю"
)

_DEFER_RE = re.compile(
    # обещание времени: «дай(те) немного времени / пару минут / терпения»
    r"дай(?:те)?\s+(?:мне\s+)?(?:немного\s+|пару\s+|чуть\s+)?(?:врем|минут|секунд|терпени)"
    r"|мне\s+(?:нужно|потребуется|понадобится)\s+(?:немного\s+)?(?:врем|минут|секунд)"
    r"|это\s+займ[еёет]+\s+(?:немного\s+)?(?:врем|минут)"
    r"|немного\s+терпени"
    r"|подожди(?:те)?\s+(?:немного|чуть|пожалуйста|секунд|минут)"
    # «сейчас/потом/скоро/позже (я) <будущий глагол>» — ядро отложенной работы
    rf"|(?:сейчас|щас|счас|потом|позже|чуть\s+позже|скоро|дальше)\s+(?:я\s+)?(?:{_DEFER_VERB})"
    rf"|(?:{_DEFER_VERB})\s+(?:его|её|их|тебе|вам|список|результат|инфу|информаци"
    r"|данн|варианты|подробн|чуть\s+позже|позже|попозже|потом|в\s+ближайш)"
    r"|(?:работаю|работаем|трудимся)\s+над\s+этим"
    r"|скоро\s+(?:вернусь|отпишу|пришлю|сообщу|расскажу|будет\s+готов)"
    r"|как\s+только\s+(?:найду|соберу|закончу|проверю|узнаю|подготовлю)"
    r"|(?:дам\s+знать|отпишу(?:сь)?|вернусь\s+с)\b",
    re.IGNORECASE,
)

# Фальшивое прошедшее: модель заявляет, будто уже искала/смотрела в интернете.
# Этот детектор вызывают ТОЛЬКО когда за ход не было ни одного инструмента —
# значит «я искал в интернете / нашёл онлайн» заведомо ложь, а не деферрал.
_FAKE_SEARCH_RE = re.compile(
    r"(?:я\s+)?(?:искал|поискал|наш[её]л|посмотрел|проверил|погуглил|погуглил|"
    r"просмотрел|изучил|собрал)\b[^.]{0,40}?"
    r"(?:в\s+интернете|в\s+сети|в\s+гугле|онлайн|в\s+вебе|в\s+поиске)",
    re.IGNORECASE,
)


def is_deferral(reply_text: str) -> bool:
    """Ответ обещает будущую работу ИЛИ врёт про уже сделанный поиск? Без LLM.

    Вызывающий обязан проверять это ТОЛЬКО когда за ход не было вызовов
    инструментов, — тогда и обещание заведомо пустое (фоновой работы у бота
    нет), и заявление «я искал в интернете» заведомо ложное.
    """
    text = (reply_text or "").strip()
    if not text:
        return False
    return bool(_DEFER_RE.search(text) or _FAKE_SEARCH_RE.search(text))


NO_DEFER_NUDGE = (
    "[Системное: ты не выполняешь ничего в фоне — этот ход синхронный. Ты "
    "пообещал сделать работу «потом» («сейчас поищу», «дай мне время» и т.п.), "
    "но не вызвал ни одного инструмента, а значит не сделал ничего и никто к "
    "задаче не вернётся. Сделай запрошенное ПРЯМО СЕЙЧАС: вызови нужные "
    "инструменты (web_search, fetch_url и т.д.) в этом же ходе и ответь готовым "
    "результатом. Если выполнить нельзя — честно скажи, почему, без обещаний на "
    "будущее.]"
)


def last_user_text(messages: list[dict]) -> str:
    """Текст последней реплики пользователя из собранного контекста (пропускаем
    мультимодальные списки и системные пометки в квадратных скобках)."""
    for msg in reversed(messages):
        if msg.get("role") != "user":
            continue
        content = msg.get("content")
        if isinstance(content, str) and not content.lstrip().startswith("["):
            return content
    return ""
